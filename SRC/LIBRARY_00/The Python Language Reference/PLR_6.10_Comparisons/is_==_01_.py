    # ------------------------------------------------------
    # Ловушки и тонкости сравнения значений и идентичности
    # ------------------------------------------------------
    #
    # В Python оператор == сравнивает значения объектов и
    # возвращает True, если значения равны.
    #
    # Оператор is сравнивает идентичность объектов и возвращает
    # True, если оба операнда ссылаются на один и тот же объект в
    # памяти.
    #
    # Некоторые тонкости использования операторов == и is в
    # Python:
    #
    # 1. Сравнение целых чисел в диапазоне от -5 до 256:
    # - В Python целые числа в диапазоне от -5 до 256 кэшируются,
