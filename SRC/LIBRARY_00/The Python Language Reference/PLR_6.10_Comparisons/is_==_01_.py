    # объекты.
    #
    # 2. Контейнеры:
    # - Для контейнеров, таких как списки, множества и словари,
    # оператор is сравнивает их идентичность, проверяя, указывают
    # ли переменные на один и тот же физический объект в памяти.
    #
    # 3. Сравнение с None:
    # - Для проверки на равенство None рекомендуется использовать
    # ==, так как None в является единственным экземпляром объекта
    # типа None. Использование is может дать верный результат для
    # None, но может вызвать проблемы при сравнении с другими
    # объектами.

