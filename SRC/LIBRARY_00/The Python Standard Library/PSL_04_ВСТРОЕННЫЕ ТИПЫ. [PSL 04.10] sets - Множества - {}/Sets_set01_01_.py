    # 5. difference() - Разность множеств

    # Метод difference() возвращает элементы, которые есть в одном
    # множестве, но отсутствуют в другом.
    set1 = {1, 2, 3, 4, 5}
    set2 = {4, 5, 6, 7, 8}
