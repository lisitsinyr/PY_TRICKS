    # https://t.me/pythonercode/1638
    # ✈️Сегодня поговорим об асинхронных генераторах — фиче,
    # которая может серьёзно прокачать ваш код. Но для начала
    # давайте разберёмся, что это за зверь такой.
    #
    # ➡️Что такое асинхронный генератор?
    #
    # Представьте обычный генератор, но на стероидах. Асинхронный
    # генератор — это функция, которая использует async def и
    # yield для создания асинхронного итератора. Звучит сложно? На
    # самом деле, это просто способ лениво создавать
    # последовательность значений, не блокируя основной поток
    # выполнения.
    #
    # ➡️Зачем они нужны?
    #
    # Асинхронные генераторы особенно полезны, когда вы работаете
    # с I/O-bound задачами. Например, при обработке больших
    # объёмов данных из сети или файловой системы. Они позволяют
    # эффективно управлять памятью и повышают отзывчивость
    # приложения.
    #
    # ➡️Как это выглядит на практике?
    # async def async_range(start, stop):
    #     for i in range(start, stop):
    #         await asyncio.sleep(0.1)
    #         yield i
    #
    # async def main():
    #     async for num in async_range(0, 5):
    #         print(num)
    #
    # asyncio.run(main())
    #
    # ⬆️В этом примере async_range имитирует долгую операцию с
    # помощью asyncio.sleep. В реальном коде вместо sleep могла бы
    # быть работа с базой данных или API.
    #
    # ➡️Где это реально пригодится?
    #
    # 1. Парсинг данных: Представьте, что вам нужно обработать
    # гигабайты логов. Асинхронный генератор позволит читать и
    # обрабатывать данные порциями, не загружая всё в память
    # разом.
    # 2. Стриминг данных: При работе с потоковым API вы можете
    # использовать асинхронный генератор для обработки данных по
    # мере их поступления.
    # 3. Пагинация: Если вы работаете с API, которое возвращает
    # данные постранично, асинхронный генератор может
    # абстрагировать логику пагинации, предоставляя удобный
    # интерфейс для работы с данными.
    #
    # ➡️Подводные камни
    #
    # Помните, что асинхронные генераторы работают только внутри
    # асинхронного кода. Их нельзя использовать в синхронных
    # функциях. Кроме того, они могут быть сложнее для понимания и
    # отладки, особенно для новичков в асинхронном
    # программировании.

