    # 🤔 Что такое @dataclass?
    #
    # Это декоратор, предоставленный модулем dataclasses, который
    # автоматически генерирует специальные методы, такие как
    # __init__, __repr__, __eq__, и другие, для вашего класса. Это
    # упрощает создание классов, предназначенных для хранения
    # данных, устраняя необходимость писать много шаблонного кода.
    #
    # 🚩Зачем нужен
    #
    # 🟠Упрощение кода
    # Автоматически генерирует методы, сокращая шаблонный код.
    # 🟠Читабельность
    # Делает код более чистым и легким для понимания.
    # 🟠Удобство
    # Обеспечивает удобные и мощные возможности для работы с
    # данными.
    #
    # 🚩Как использовать
    #
    # Нужно импортировать его из модуля dataclasses и применить к
    # классу. Внутри класса достаточно определить только поля
    # данных.
    # from dataclasses import dataclass
    #
    # @dataclass
    # class Person:
    #     name: str
    #     age: int
    #
    # # Примеры использования
    # person1 = Person(name="Alice", age=30)
    # person2 = Person(name="Bob", age=25)
    #
    # print(person1)  # Вывод: Person(name='Alice', age=30)
    # print(person2)  # Вывод: Person(name='Bob', age=25)
    # print(person1 == person2)  # Вывод: False
    #
    # 🚩Автоматически генерируемые методы
    #
    # 🟠`__init__`
    # Инициализирует объект с заданными значениями атрибутов.
    # 🟠`__repr__`
    # Возвращает строковое представление объекта, удобное для
    # отладки.
    # 🟠`__eq__`
    # Сравнивает объекты на равенство по их атрибутам.
    # 🟠`__lt__`, `__le__`, `__gt__`, `__ge__`
    # Могут быть сгенерированы для сравнения объектов (если
    # указано).
    #
    # 🚩Настройка поведения
    #
    # Вы можете настроить поведение @dataclass с помощью
    # параметров, таких как order, frozen, и других.
    #
    # 🟠`order=True`
    # Генерирует методы для сравнения объектов.
    # 🟠`frozen=True`
    # Делает экземпляры неизменяемыми (immutable).
    #
    # Пример
    # from dataclasses import dataclass
    #
    # @dataclass(order=True, frozen=True)
    # class Person:
    #     name: str
    #     age: int
    #
    # person1 = Person(name="Alice", age=30)
    # person2 = Person(name="Bob", age=25)
    #
    # print(person1 > person2)  # Вывод: True (поскольку 'Alice' >
    # 'Bob' по алфавиту, если имена равны, сравниваются возраста)
    # # person1.age = 31  # Ошибка: FrozenInstanceError (поскольку
